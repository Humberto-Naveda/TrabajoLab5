    public BuscarCliente() {
        initComponents();
        jlistTel.setModel(modeloListaTel);
        llenarListaTelefonos();
        
        jlistTel.addListSelectionListener(evt -> {
            if (!evt.getValueIsAdjusting()) {
                Long telefono = jlistTel.getSelectedValue();
                if (telefono != null) {
                    mostrarCliente(telefono);
                }
            }
        });
                }
