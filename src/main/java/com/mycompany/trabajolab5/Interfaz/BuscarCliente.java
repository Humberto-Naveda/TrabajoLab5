    private void llenarListaTelefonos(){
        DefaultListModel<String> modelo = new DefaultListModel<>();
        for(Long tel : Directorio.contactos.keySet()){
            modelo.addElement(String.valueOf(tel));
        }
        jlLista.setModel(modelo);
    }
    private void autoRellenoLista(){
        jlLista.addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                String telSeleccionado = jlLista.getSelectedValue();
                if (telSeleccionado != null) {
                    Long telefono = Long.parseLong(telSeleccionado);
                    Contacto c = Directorio.contactos.get(telefono);

                    if (c != null) {
                        txtTelefono.setText(String.valueOf(telefono));
                        txtDni.setText(c.getDni());
                        txtNombre.setText(c.getNombre());
                        txtApellido.setText(c.getApellido());
                        txtCiudad.setText(c.getCiudad());
                        txtDomicilio.setText(c.getDireccion());
                    }
                }
            }
        });
    }

